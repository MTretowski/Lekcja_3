public class Main {
  public static void main(String[] args) {
    System.out.println("XYZ");
    System.out.println("ABC");
    System.out.println("ZZZ");
    System.out.println("123");

    for (int i = 0; i < 5; i++) {
      System.out.println(i);
    }

    System.out.println("XYZ");
    System.out.println("ABC");
    System.out.println("ZZZ");

    int j = 5;
    do {
      System.out.println(j);
      j--;
    } while (j > 0);
  }
}
